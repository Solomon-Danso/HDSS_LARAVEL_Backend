<?php
namespace App\Jobs;

use App\Models\Teacher;
use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Foundation\Bus\Dispatchable;
use Illuminate\Queue\InteractsWithQueue;
use Illuminate\Queue\SerializesModels;
use Maatwebsite\Excel\Facades\Excel;

class UploadingAutoGeneratedTeacherList implements ShouldQueue
{
    use Dispatchable, InteractsWithQueue, Queueable, SerializesModels;

    protected $filePath;
    protected $CompanyId;

    public function __construct($filePath, $CompanyId)
    {
        $this->filePath = $filePath;
        $this->CompanyId = $CompanyId;
    }

    public function handle()
    {
        $rows = Excel::toArray([], $this->filePath, null, \Maatwebsite\Excel\Excel::XLSX);
    
        if (count($rows) > 0) {
            $headers = $rows[0][0]; // Assuming the headers are in the first row
    
            for ($i = 1; $i < count($rows[0]); $i++) {
                $row = $rows[0][$i];
    
                // Check if TeacherId is defined in the file
                $TeacherId = isset($row[0]) ? $row[0] : null;
    
                // Check if TeacherId already exists
                $existingTeacher = Teacher::where('TeacherId', $TeacherId)->where('CompanyId', $this->CompanyId)->first();
    
                if ($existingTeacher) {
                    continue; // Skip if the TeacherId already exists
                }
    
                $s = new Teacher();
    
                // Assign values based on headers
                $s->CompanyId = $this->CompanyId;
    
                // Generate TeacherId if not defined in the file
                $s->TeacherId = $TeacherId ? $TeacherId : strval(10000 + $s->id);
    
                foreach ($headers as $index => $header) {
                    $s->{$header} = $row[$index];
                }
    
                $s->Role = "Teacher";
    
                $s->save();
            }
        }
    }
}
